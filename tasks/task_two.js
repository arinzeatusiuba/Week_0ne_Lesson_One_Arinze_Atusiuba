const num1 = 1;
const num2 = 0;

let result;

result = num1 + num2;
console.log(result); // Outputs 1

result = num1 - num2;
console.log(result); // Outputs 1

result = num1 * num2;
console.log(result); // Outputs 0

result = num1 / num2;
console.log(result); // Outputs Infinity

result = num1 % num2;
console.log(result); // Outputs NaN

